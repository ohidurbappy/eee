org 100H
.DATA
A DW 012H          
B DW 025H
.CODE
MAIN PROC
;ASSUME CS: CODE
MOV AX, A
MOV BX, B
MOV CX,0H
MOV DX,0H
CMP BX,AX
JLE CHANGED
XCHG AX,BX
MOV A,AX
MOV B,BX
CHANGED:
LEV1:
ADD BX,B
CMP BX,AX
JL LEV1
JG LEV2 
JE DONE1
LEV2:
ADD AX,A
CMP AX,BX
JL LEV2
JG LEV1 
JE DONE2
DONE1: MOV BX,AX
DONE2: MOV AX,AX
        END